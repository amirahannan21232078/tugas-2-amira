class LogicalOperations {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        // Operator AND
        System.out.println(a + " && " + b + " = " + (a && b));

        // Operator OR
        System.out.println(a + " || " + b + " = " + (a || b));

        // Operator NOT
        System.out.println("!" + a + " = " + (!a));
    }
}